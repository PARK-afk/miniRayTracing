#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		i;

	if (fd < 0)
		return ;
	i = 0;
	if (s[i])
	{
		while (s[i])
		{
			write (fd, &s[i], 1);
			i++;
		}
	}
}
